# ======================================================================
# V7: 非线性 EOR 曲线 + 结构 EV 计算（写死 rank_counts 语义）
# ----------------------------------------------------------------------
# 语义约定：
#   - total_decks: 满鞋副数（通常为 8）；
#   - rank_counts[r]: 当前鞋中点数 r (1..9) 剩余的“绝对张数”；
#   - 对于结构 EV：
#         base_per_rank = total_decks * 4  （这里只考虑 1..9 点，0 点牌不纳入 SBI 结构）
#         missing_k[r]  = max(0, base_per_rank - rank_counts[r])
#     即 missing_k[r] ∈ [0, 32]，代表从“完整 8 副牌”视角看，
#     rank=r 已经被永久移除的张数；我们显式不考虑“剩余多于理论值”的场景，
#     一旦 base_per_rank - 当前剩余 < 0，就按 0 处理。
#
#   - CURVE_DELTA_EV_P[r][k]: 删除 k 张 rank=r 后，押闲 EV 相对理论基准的偏移；
#   - CURVE_DELTA_EV_B_COMM[r][k]: 对押庄 EV(含5%抽水) 的偏移。
#   - SBI_P / SBI_B_comm：把每个 rank 的非线性 ΔEV(r, k) 在当前 missing_k[r] 处取值再累加。
# ======================================================================


def _clamp_missing_k(k: int) -> int:
    """将缺失张数 k 约束到 [0, 32]。"""
    if k <= 0:
        return 0
    if k >= 32:
        return 32
    return k


def _lookup_curve_delta(curve: dict, rank: int, missing_k: int) -> float:
    """
    安全查表：missing_k==0 时返回 0；1..32 直接查表。
    若极端情况下表中缺少某个 k，则回退到 “不大于 missing_k 的最大 key”。
    """
    k = _clamp_missing_k(missing_k)
    if k == 0:
        return 0.0

    rank_map = curve.get(rank)
    if not rank_map:
        return 0.0

    if k in rank_map:
        return rank_map[k]

    smaller = [kk for kk in rank_map.keys() if kk <= k]
    if not smaller:
        return 0.0
    use_k = max(smaller)
    return rank_map[use_k]


